# a list is an ordered collection (you can change items)
fruits = ["apple", "Banana", "Cherry"]
print(fruits[0]) #  access apple
fruits.append("orange") # add orange to the list
fruits.remove("Banana") # removes banana from the list
print(fruits)


colors=["Red","Orange", "Violet", "Blue", "Black"]
